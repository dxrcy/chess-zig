const LargeLetter = *const [HEIGHT * WIDTH + (HEIGHT - 1)]u8;

pub fn translateSymbol(symbol: u8, ascii: bool) u21 {
    return if (ascii) switch (symbol) {
        '#' => '%',
        ',' => ',',
        '\'' => '\'',
        ']' => '%',
        '[' => '%',
        '.' => ' ',
        else => unreachable,
    } else switch (symbol) {
        '#' => '█',
        ',' => '▄',
        '\'' => '▀',
        ']' => '▐',
        '[' => '▌',
        '.' => ' ',
        else => unreachable,
    };
}

